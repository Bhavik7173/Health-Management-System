async def seed_medicine_database():
    print("┌─────────────────────────────────────────────────────────────────────┐")
    print("│        MEDICORE AI — Medicine Database Seed Script                  │")
    print(f"│        MongoDB: {MONGO_URL:<53}│")
    print(f"│        Database: {DB_NAME:<52}│")
    print("└─────────────────────────────────────────────────────────────────────┘")
    print()

    try:
        # Test connection
        await db.command("ping")
        print("✅ Connected to MongoDB")
    except Exception as e:
        print(f"❌ Cannot connect to MongoDB: {e}")
        print(f"   Make sure MongoDB is running at: {MONGO_URL}")
        return

    # Count existing
    existing = await db.medicine_db.count_documents({})
    print(f"📦 Existing records in medicine_db: {existing}")

    if existing > 0:
        await db.medicine_db.delete_many({})
        print(f"🗑️  Cleared {existing} existing records")

    # Insert all medicines
    result = await db.medicine_db.insert_many(MEDICINES)
    print(f"\n✅ SUCCESS! Inserted {len(result.inserted_ids)} medicines into MongoDB")
    print(f"   Collection: {DB_NAME}.medicine_db")
    print()
    print("📋 Medicines added:")
    for i, med in enumerate(MEDICINES):
        brands = ", ".join(med["brandNames"][:3])
        rx = "Rx" if med["requiresPrescription"] else "OTC"
        print(f"   {i+1:2}. {med['name']:<35} [{med['category']:<30}] ({rx})")
        print(f"       Brands: {brands}")
        print(f"       Treats: {', '.join(med['symptoms'][:4])}")
        print(f"       Home remedies: {len(med['homeRemedies'])}")
        print()

    print("🎉 Medicine database seeding complete!")
    print()
    print("These medicines are now accessible via:")
    print("   GET  /api/medicine-db/list")
    print("   GET  /api/medicine-db/categories")
    print("   GET  /api/medicine-db/{id}")
    print("   POST /api/medicine-db/symptom-search  — search by symptom/disease")
    print()


